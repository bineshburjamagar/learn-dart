void main() {
  //integer
  var amount1 = 100;
  int amount2 = 101;

  print('amount1 = $amount1 | amount2 = $amount2');

  //boolean
  var bol1 = true;
  bool bol2 = false;

  print('bol1= $bol1 | bol2 = $bol2 ');

  //string
  var string1 = 'String 1';
  String string2 = 'String 3';

  print('string1 = $string1 | string2 = $string2');

  //dynamic
  dynamic weakVariable = 1000;
  print('wweakVariable = $weakVariable');

  weakVariable = "This is weak variable";
}
